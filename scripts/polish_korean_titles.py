def apply_title_overrides(data: dict[str, Any]) -> int:
    changed = 0
    for row in data.get("qualitativeRows", []):
        question = str(row.get("questionNumber", ""))
        title = TITLE_OVERRIDES.get(question)
        if not title:
            continue
        if row.get("title_ko") != title:
            row["title_ko"] = title
            changed += 1
        quality = row.setdefault("translationQuality", {})
        quality["koreanTitlePolisher"] = {
            "version": 1,
            "status": "curated-title",
        }
    return changed


def main() -> None:
    data = json.loads(DATASET.read_text(encoding="utf-8"))
    changed = apply_title_overrides(data)
    DATASET.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(json.dumps({"changedTitles": changed}, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
